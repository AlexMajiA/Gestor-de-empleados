    private void Bt_hireActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_hireActionPerformed

        //Obtengo los valores de los JTextFields de tipo String
        int code = 0;
        String name = Tf_name.getText();
        String department = Tf_department.getText();
        double salary = 0.0;

        //Valido que los campos no estÃ©n vacÃ­os, en caso contrario muestro el error.
        if (name.isEmpty() || department.isEmpty()) {
            Lb_error.setText("Error: No puedes dejar campos vacÃ­os.");
            return;
        }

        try {
            //Convierto los datos obtenidos en los tipos correctos.
            salary = Double.parseDouble(Tf_salary.getText());
            code = Integer.parseInt(Tf_code.getText());

            //Una vez que obtengo los campos, compruebo que no sean cero y ni estÃ©n vacÃ­os.
            if (code <= 0) {
                Lb_error.setText("Error: CÃ³digo no puede ser 0 o estÃ¡r vacÃ­o.");
                return;
            }

            if (salary <= 0) {
                Lb_error.setText("Error: Salario no puede ser 0 o estÃ¡r vacÃ­o.");
                return;
            }

        } catch (NumberFormatException e) {
            Lb_error.setText("Error: Salario y CÃ³digo no pueden estar vacÃ­os y deben ser nÃºmeros vÃ¡lidos.");
            return;
        }

        String result = EM.newEmployee(code, name, salary, department);

        if (result != null && !result.isEmpty()) {
            // Limpiar los campos de entrada solo si la operaciÃ³n fue exitosa
            clearFields();
            Lb_error.setText("Empleado agregado correctamente");
        }

    }//GEN-LAST:event_Bt_hireActionPerformed

    private void Bt_dismissActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_dismissActionPerformed
        // Obtengo el valor del cÃ³digo del empleado a despedir.
        String code = Tf_code.getText();

        if (code.trim().isEmpty()) {
            Lb_error.setText("El cÃ³digo no puede ser cero ni estar vacÃ­o.");
            return;
        }

        int newCode; 
        
        try {
            //Obtengo el valor introducido.
            newCode = Integer.parseInt(code);

            //ValidaciÃ³n de que el codigo sea vÃ¡lido.
            if (newCode <= 0) {
                Lb_error.setText("el cÃ³digo debe ser mayor que cero.");
                return;
            }

        } catch (NumberFormatException e) {
            Lb_error.setText("Error: El cÃ³digo debe ser un nÃºmero entero vÃ¡lido.");
            return;
            
        }
        
        String result = null;
        try {
            result = EM.dissmisEmployee(newCode);
        } catch (SQLException ex) {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (result != null && !result.isEmpty()) {
            clearFields();
             Lb_error.setText("Empleado despedido correctamente");
        }
    }//GEN-LAST:event_Bt_dismissActionPerformed

